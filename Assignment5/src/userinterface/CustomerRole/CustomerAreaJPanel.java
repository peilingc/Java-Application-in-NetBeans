    public void populateTblFood(Restaurant r){
        DefaultTableModel dtm = (DefaultTableModel)tblFood.getModel();
        dtm.setRowCount(0);
        for(String s : r.getMenu()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=s;
            dtm.addRow(row);
        }
    }
    
