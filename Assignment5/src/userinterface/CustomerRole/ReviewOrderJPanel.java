    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtArea.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Please give reviews!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        userAccount.getWorkQueue().getOrderRequestList().get(selectedRow).setMessage(txtArea.getText());
        JOptionPane.showMessageDialog(null, "Successfully submitted!");
        txtArea.setEnabled(false);
        btnSubmit.setEnabled(false);
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        selectedRow = tblOrder.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!tblOrder.getValueAt(selectedRow, 2).equals("Finished")){
            JOptionPane.showMessageDialog(null, "Order has not completed. Please give reviews after the order is finished!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        txtArea.setEnabled(true);
        btnSubmit.setEnabled(true);
    }//GEN-LAST:event_btnSelectActionPerformed

