    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRM.getModel();
        model.setRowCount(0);
        for (RestaurantManager r : system.getRestaurantManagerDirectory().getRestaurantManagerList()) {
            Object[] row = new Object[4];
            row[0] = r;
            row[1] = r.getRestaurant();
            row[2] = r.getId();
            row[3] = r.getUsername();
            model.addRow(row);
        }
    }
    
