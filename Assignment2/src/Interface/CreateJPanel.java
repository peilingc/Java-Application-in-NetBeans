    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: 
        //all textfields must be entered
        if (txtSerialNum.getText().length()==0 ||
            txtBrand.getText().length()==0 || 
            txtModelNum.getText().length()==0 || 
            txtCity.getText().length()==0 || 
            txtYOM.getText().length()==0 ||
            txtCert.getText().length()==0 ){
            JOptionPane.showMessageDialog(null, "Please ensure all fields are entered!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //prevent serial number duplicates
        else if (!carCatalog.searchSerialNum(txtSerialNum.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "The serial number has already existed!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //ensure availability checkbox
        else if (!checkAvail.isSelected() && !checkUnavail.isSelected()){
            JOptionPane.showMessageDialog(null, "Please check the availability!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //ensure seats checkbox
        else if (!check2.isSelected() && !check3.isSelected() && 
                 !check4.isSelected() && !check5.isSelected() && 
                 !check6.isSelected() && !check7.isSelected() &&
                 !check8.isSelected() && !check9.isSelected() ){
            JOptionPane.showMessageDialog(null, "Please check the seats!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //restrict input with int type and localDate type
        else if (isInteger(txtSerialNum.getText()) & isInteger(txtYOM.getText()) & isLocalDate(txtCert.getText())){
            //get text from textfields and assign to memory
            String serialNum = txtSerialNum.getText();
            String brand = txtBrand.getText();
            String modelNum = txtModelNum.getText();
            String city = txtCity.getText();
            String yom = txtYOM.getText();
            LocalDate cert = LocalDate.parse(txtCert.getText());
            boolean availability = checkAvail.isSelected();
            int seat=0;
            if (check2.isSelected()){
                seat = 2;
            }
            else if (check3.isSelected()){
                seat = 3;
            }
            else if (check4.isSelected()){
                seat = 4;
            }
            else if (check5.isSelected()){
                seat = 5;
            }
            else if (check6.isSelected()){
                seat = 6;
            }
            else if (check7.isSelected()){
                seat = 7;
            }
            else if (check8.isSelected()){
                seat = 8;
            }
            else
                seat = 9;
            
            
            //add the car to carCatalog
            Car car = carCatalog.addCar();
            car.setSerialNum(serialNum);
            car.setBrand(brand);
            car.setModelNum(modelNum);
            car.setCity(city);
            car.setManufactureYear(yom);
            car.setCertExpriedDate(cert);
            car.setAvailability(availability);
            car.setSeats(seat);
            
            JOptionPane.showMessageDialog(null, "The car has successfully created!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

