    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblCustomer.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Customer c = (Customer) tblCustomer.getValueAt(row, 0);
        system.getCustomerDirectory().getCustomerList().remove(c);
        system.getUserAccountDirectory().getUserAccountList().remove(system.getUserAccountDirectory().findUser(c.getUsername()));
        JOptionPane.showMessageDialog(null, "Successfully deleted!");
        populateTable();  
        
        txtName.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblCustomer.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        btnSubmit.setEnabled(true);
        btnCreate.setEnabled(false);
        txtUsername.setEnabled(false);
        
        updateCustomer = (Customer) tblCustomer.getValueAt(row, 0);
        updateUA = system.getUserAccountDirectory().findUser(updateCustomer.getUsername());
        txtName.setText(updateCustomer.getName());
        txtUsername.setText(updateCustomer.getUsername());
        txtPassword.setText(updateUA.getPassword());
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        updateCustomer.setName(txtName.getText());
        updateUA.setPassword(txtPassword.getText());
        JOptionPane.showMessageDialog(null, "Successfully updated!");
        populateTable();  
        
        
        btnSubmit.setEnabled(false);
        btnCreate.setEnabled(true);
        txtUsername.setEnabled(true);
        
        txtName.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().length()==0 || txtUsername.getText().length()==0 || txtPassword.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter all fields!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        if (!system.getUserAccountDirectory().checkIfUsernameIsUnique(txtUsername.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a unique username!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        system.getCustomerDirectory().createCustomer(txtName.getText(), txtUsername.getText());
        system.getUserAccountDirectory().createUserAccount(txtUsername.getText(), txtPassword.getText(), userAccount.getEmployee(), new CustomerRole());
        JOptionPane.showMessageDialog(null, "Successfully created!");
        populateTable();  
        
        txtName.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

