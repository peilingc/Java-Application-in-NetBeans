    public void populateTable(WorkQueue wq){
        DefaultTableModel dtm = (DefaultTableModel)tblOrder.getModel();
        dtm.setRowCount(0);
        for(OrderRequest or : wq.getOrderRequestList()){
            Object[] row = new Object[3];
            for (Restaurant r : system.getRestaurantDirectory().getRestaurantList()){
                if (r.getRm().getUsername().equals(or.getReceiver().getUsername())){
                    row[0] = r;
                }
            }
            
            row[1] = or.getOrderItem();
            row[2] = or.getStatus();
            dtm.addRow(row);
        }
    }

