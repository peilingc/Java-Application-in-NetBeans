    public boolean isInteger( String input ){ 
        try { 
            Integer.parseInt( input ); 
            return true; 
        } 
        catch( Exception e ) {  
            JOptionPane.showMessageDialog(null, "Please enter only numbers for serial number and year of manufacture!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false ;
        } 
    }
    
    public boolean isLocalDate( String input ){ 
        try { 
            LocalDate.parse( input ); 
            return true; 
        } 
        catch( Exception e ) {  
            JOptionPane.showMessageDialog(null, "Please enter a valid date!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false ;
        } 
    }
    

