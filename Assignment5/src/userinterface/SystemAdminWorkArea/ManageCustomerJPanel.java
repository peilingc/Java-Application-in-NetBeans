    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
        model.setRowCount(0);
        for (Customer c : system.getCustomerDirectory().getCustomerList()) {
            Object[] row = new Object[3];
            row[0] = c;
            row[1] = c.getId();
            row[2] = c.getUsername();
            model.addRow(row);
        }
    }
    

