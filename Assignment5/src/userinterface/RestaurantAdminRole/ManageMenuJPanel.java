    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
        model.setRowCount(0);
        for (String s : restaurant.getMenu()){
            Object[] row = new Object[1];
            row[0] = s;
            model.addRow(row);
        }
    }

