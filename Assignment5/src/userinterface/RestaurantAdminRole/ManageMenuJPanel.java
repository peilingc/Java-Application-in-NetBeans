    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblMenu.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String s = (String) tblMenu.getValueAt(row, 0);
        restaurant.getMenu().remove(s);
        JOptionPane.showMessageDialog(null, "Successfully deleted!");
        populateTable();     
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtFood.getText().length()<0){
            JOptionPane.showMessageDialog(null, "Please enter the field!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        restaurant.getMenu().add(txtFood.getText());
        JOptionPane.showMessageDialog(null, "Successfully added!");
        txtFood.setText("");
        populateTable();
    }//GEN-LAST:event_btnAddActionPerformed

