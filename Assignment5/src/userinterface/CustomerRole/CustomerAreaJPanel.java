    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        String selectedR = jComboBox.getSelectedItem().toString();
        ArrayList<Restaurant> list = system.getRestaurantDirectory().getRestaurantList();
        for (int i=0; i < list.size();i++){
            if (list.get(i).toString().equals(selectedR) ){
                populateTblFood(list.get(i));
                restaurant = list.get(i);
            }
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:
        
        if(tblFood.getSelectedRowCount()<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            int row = tblFood.getSelectedRow();
            String orderItem = (String) tblFood.getValueAt(row, 0);
           
            OrderRequest orderRequest = new OrderRequest();
            orderRequest.setOrderItem(orderItem);
            orderRequest.setSender(userAccount);
            orderRequest.setStatus("Unapproved");
            
            for(UserAccount ua : system.getUserAccountDirectory().getUserAccountList()){
                if (ua.getUsername().equals(restaurant.getRm().getUsername())){
                    orderRequest.setReceiver(ua);
                    ua.getWorkQueue().getOrderRequestList().add(orderRequest);
                }
            }
            userAccount.getWorkQueue().getOrderRequestList().add(orderRequest);
            
            JOptionPane.showMessageDialog(null, "Successfully ordered!");
        }
    }//GEN-LAST:event_btnOrderActionPerformed

