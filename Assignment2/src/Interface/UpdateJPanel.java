    public boolean isInteger( String input ){ 
        try { 
            Integer.parseInt( input ); 
            return true; 
        } 
        catch( Exception e ) {  
            JOptionPane.showMessageDialog(null, "Please enter only numbers for year of manufacture!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false ;
        } 
    }
    
    public boolean isLocalDate( String input ){ 
        try { 
            LocalDate.parse( input ); 
            return true; 
        } 
        catch( Exception e ) {  
            JOptionPane.showMessageDialog(null, "Please enter a valid date!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false ;
        } 
    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblUpdate.getModel();
        dtm.setRowCount(0);
        for (Car cars: carCatalog.getCarList()){    
            Object row[] = new Object[8];
            row[0] = cars;
            row[1] = cars.getModelNum();
            row[2] = cars.getCity();
            row[3] = cars.isAvailable();
            row[4] = cars.getManufactureYear();
            row[5] = cars.getSeats();
            row[6] = cars.getCertExpriedDate();
            row[7] = cars.getSerialNum();
            dtm.addRow(row); 
        }
    }
    
