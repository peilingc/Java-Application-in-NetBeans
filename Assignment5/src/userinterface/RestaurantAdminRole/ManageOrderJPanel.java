    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (cbAccept.isSelected()){
            orderRequest.setStatus("Approved");
        }
        else
            orderRequest.setStatus("Declined");
        
        for (DeliveryMan d : system.getDeliveryManDirectory().getDeliveryManList()){
            if (d.toString().equals(jComboBox.getSelectedItem())){
                orderRequest.setDeliveryMan(d);
                system.getUserAccountDirectory().findUser(d.getUsername()).getWorkQueue().getOrderRequestList().add(orderRequest);
            }
        }
        
        JOptionPane.showMessageDialog(null, "Successfully Submitted!!");
        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed


