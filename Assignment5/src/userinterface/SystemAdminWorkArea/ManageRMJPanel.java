    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblRM.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        RestaurantManager r = (RestaurantManager) tblRM.getValueAt(row, 0);
        system.getRestaurantDirectory().getRestaurantList().remove(r.getRestaurant());
        system.getRestaurantManagerDirectory().getRestaurantManagerList().remove(r);
        system.getUserAccountDirectory().getUserAccountList().remove(system.getUserAccountDirectory().findUser(r.getUsername()));
        JOptionPane.showMessageDialog(null, "Successfully deleted!");
        populateTable();
        
        txtName.setText("");
        txtRestaurant.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblRM.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        btnSubmit.setEnabled(true);
        btnCreate.setEnabled(false);
        txtRestaurant.setEnabled(false);
        txtUsername.setEnabled(false);

        updateRM = (RestaurantManager) tblRM.getValueAt(row, 0);
        updateUA = system.getUserAccountDirectory().findUser(updateRM.getUsername());
        txtName.setText(updateRM.getName());
        txtRestaurant.setText(updateRM.getRestaurant().toString());
        txtUsername.setText(updateRM.getUsername());
        txtPassword.setText(updateUA.getPassword());
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().length()==0 || 
            txtRestaurant.getText().length()==0 || 
            txtUsername.getText().length()==0 ||
            txtPassword.getText().length()==0){
            
            JOptionPane.showMessageDialog(null, "Please enter all fields!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        if (!system.getUserAccountDirectory().checkIfUsernameIsUnique(txtUsername.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a unique username!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        // check restaurant exist or not
        if (system.getRestaurantDirectory().getRestaurantList()!= null){
            for (Restaurant r:system.getRestaurantDirectory().getRestaurantList()){
                if (r.getName().equals(txtRestaurant.getText())){
                    JOptionPane.showMessageDialog(null, "Duplicate Restaurant Name!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
        
        system.getRestaurantManagerDirectory().createRestaurantManager(txtName.getText(), txtUsername.getText(), txtRestaurant.getText());
        system.getUserAccountDirectory().createUserAccount(txtUsername.getText(), txtPassword.getText(), userAccount.getEmployee(), new AdminRole());
        JOptionPane.showMessageDialog(null, "Successfully created!");
        populateTable();
        
        txtName.setText("");
        txtRestaurant.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

