    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here: 
        int selectedRow = tblUpdate.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first to update details!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Car car = (Car) tblUpdate.getValueAt(selectedRow, 0);
            this.car = car;
            txtSerialNum.setText(car.getSerialNum());
            txtBrand.setText(car.getBrand());
            txtModelNum.setText(car.getModelNum());
            txtCity.setText(car.getCity());
            txtYOM.setText(car.getManufactureYear());
            txtCert.setText(car.getCertExpriedDate().toString());
            checkAvail.setSelected(car.isAvailable());
            checkUnavail.setSelected(!car.isAvailable());
            check2.setSelected(car.getSeats()==2);
            check3.setSelected(car.getSeats()==3);
            check4.setSelected(car.getSeats()==4);
            check5.setSelected(car.getSeats()==5);
            check6.setSelected(car.getSeats()==6);
            check7.setSelected(car.getSeats()==7);
            check8.setSelected(car.getSeats()==8);
            check9.setSelected(car.getSeats()==9);
            
            txtBrand.setEnabled(true);
            txtModelNum.setEnabled(true);
            txtCity.setEnabled(true);
            txtYOM.setEnabled(true);
            txtCert.setEnabled(true);
            checkAvail.setEnabled(true);
            checkUnavail.setEnabled(true);;
            check2.setEnabled(true);
            check3.setEnabled(true);
            check4.setEnabled(true);
            check5.setEnabled(true);
            check6.setEnabled(true);
            check7.setEnabled(true);
            check8.setEnabled(true);
            check9.setEnabled(true);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here: 
        //all fields must be entered
        if (txtBrand.getText().length()==0 ||
            txtModelNum.getText().length()==0 ||
            txtCity.getText().length()==0 ||
            txtYOM.getText().length()==0 ||
            txtCert.getText().length()==0 ){
            JOptionPane.showMessageDialog(null, "Please ensure all fields are entered!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //ensure availability checkbox
        else if (!checkAvail.isSelected() && !checkUnavail.isSelected()){
            JOptionPane.showMessageDialog(null, "Please check the availability!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //ensure seats checkbox
        else if (!check2.isSelected() && !check3.isSelected() && 
                 !check4.isSelected() && !check5.isSelected() && 
                 !check6.isSelected() && !check7.isSelected() &&
                 !check8.isSelected() && !check9.isSelected() ){
            JOptionPane.showMessageDialog(null, "Please check the seats!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
      
        //restrict input int type and localDate type
        else if (isInteger(txtYOM.getText()) && isLocalDate(txtCert.getText())){
            String serialNum = txtSerialNum.getText();
            String brand = txtBrand.getText();
            String modelNum = txtModelNum.getText();
            String city = txtCity.getText();
            String yom = txtYOM.getText();
            LocalDate cert = LocalDate.parse(txtCert.getText());
            boolean availability = checkAvail.isSelected();
            int seat=0;
            if (check2.isSelected()){
                seat = 2;
            }
            else if (check3.isSelected()){
                seat = 3;
            }
            else if (check4.isSelected()){
                seat = 4;
            }
            else if (check5.isSelected()){
                seat = 5;
            }
            else if (check6.isSelected()){
                seat = 6;
            }
            else if (check7.isSelected()){
                seat = 7;
            }
            else if (check8.isSelected()){
                seat = 8;
            }
            else
                seat = 9;

            
            car.setSerialNum(serialNum);
            car.setBrand(brand);
            car.setModelNum(modelNum);
            car.setCity(city);
            car.setManufactureYear(yom);
            car.setCertExpriedDate(cert);
            car.setAvailability(availability);
            car.setSeats(seat);

            JOptionPane.showMessageDialog(null, "The car has successfully updated!");
            populateTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

