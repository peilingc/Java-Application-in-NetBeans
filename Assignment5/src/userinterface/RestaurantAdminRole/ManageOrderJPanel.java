    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        selectedRow = tblOrder.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        orderRequest = userAccount.getWorkQueue().getOrderRequestList().get(selectedRow);
        txtName.setText(orderRequest.getSender().toString());
        txtOrder.setText(orderRequest.getOrderItem());
        txtReview.setText(orderRequest.getMessage());
        
        if (orderRequest.getStatus().equals("Approved")){
            cbAccept.setSelected(true);
            cbAccept.setEnabled(false);
            cbDecline.setEnabled(false);
            jComboBox.setEnabled(false);
        }
        else if (orderRequest.getStatus().equals("Declined")){
            cbDecline.setSelected(true);
            cbAccept.setEnabled(false);
            cbDecline.setEnabled(false);
            jComboBox.setEnabled(false);
        }
        else if (orderRequest.getStatus().equals("Finished")){
            cbAccept.setSelected(false);
            cbAccept.setEnabled(false);
            cbDecline.setEnabled(false);
            jComboBox.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnSelectActionPerformed

